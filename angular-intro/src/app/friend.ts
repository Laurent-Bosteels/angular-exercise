export class Friend {
    inputFirstname : string
    inputLastname : string
    inputEmail : string
    inputPhone : string
    inputWebsite : string
    selectFavoriteLanguage : string

    constructor(inputFirstname: string, inputLastname : string, inputEmail : string, inputPhone : string, inputWebsite : string, selectFavoriteLanguage : string){
        this.inputFirstname = inputFirstname;
        this.inputLastname = inputLastname;
        this.inputEmail = inputEmail;
        this.inputPhone = inputPhone;
        this.inputWebsite = inputWebsite;
        this.selectFavoriteLanguage = selectFavoriteLanguage;
      }

}
